print('Bem-vindo(a) ao calculador de área de paralelogramos!')

# pedindo os valores das variáveis base e altura
base = float(input('Insira o comprimento da base: '))
altura = float(input('Insira a altura: '))

# calculando o valor da área de acordo com os valores dados pelo usuário
area = base * altura

# printando na tela o valor da área com uma pequena mensagem
print('A área do paralelograma corresponde a:', area)